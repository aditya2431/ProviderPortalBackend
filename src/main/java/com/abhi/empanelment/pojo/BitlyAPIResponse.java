package com.abhi.empanelment.pojo;

public class BitlyAPIResponse {
	
	public String  LoginId;
	public String  ReturMessage;
	public String  ReturnCode;
	public String  UDP;
	public String  Identifier;
	public String  URLReturned;
	public String getLoginId() {
		return LoginId;
	}
	public void setLoginId(String loginId) {
		LoginId = loginId;
	}
	public String getReturMessage() {
		return ReturMessage;
	}
	public void setReturMessage(String returMessage) {
		ReturMessage = returMessage;
	}
	public String getReturnCode() {
		return ReturnCode;
	}
	public void setReturnCode(String returnCode) {
		ReturnCode = returnCode;
	}
	public String getUDP() {
		return UDP;
	}
	public void setUDP(String uDP) {
		UDP = uDP;
	}
	public String getIdentifier() {
		return Identifier;
	}
	public void setIdentifier(String identifier) {
		Identifier = identifier;
	}
	public String getURLReturned() {
		return URLReturned;
	}
	public void setURLReturned(String uRLReturned) {
		URLReturned = uRLReturned;
	}
	
	

}
